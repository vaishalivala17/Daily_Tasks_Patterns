// Simple Square Pattern 

#include<iostream>
using namespace std;

int main(){
    // Simple Square Pattern
    
    // *  *  *  *  * 
    // *  *  *  *  * 
    // *  *  *  *  * 
    // *  *  *  *  * 
    // *  *  *  *  * 

    for(int i=0; i<5; i++){
        for (int j=0; j<5; j++){
            cout << " * " ;
        }cout << endl;
    }

    return 0;
}
